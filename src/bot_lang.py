def translate_for_user(user_id: str, key: str, **kwargs) -> str:
    user = db.get_user(user_id)
    lang = (user[7] if user else "zh") or "zh"
    if lang not in LANGUAGES:
        lang = "zh"
    tmpl = LANGUAGES[lang].get(key) or LANGUAGES["zh"].get(key, key)
    return tmpl.format(**kwargs)
